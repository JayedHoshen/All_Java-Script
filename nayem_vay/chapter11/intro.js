// Object Oriented is just a theory
// OOP stands on four main pillar
// 1) Abstraction
// 2) Encapsulation
// 3) Inheritance
// 4) Polymorphism

// 1) What is Abstraction?
// Abstraction is one of the key concepts of object oriented programming (OOP) languages. It's main goal is to handle complexity by hiding unnecessary details from the user. That enables the user to implement more complex logic on top of the provided abstraction without understanding orr even thinking about all the hidden complexity.

// 2) What is Encapsulation?
// Encapsulation is one of the fundamental concepts in object oriented programming. It describes the idea of bundling data and methods that work on that data within one unit

// 3) What is Inheritance?
// Inheritance is one of the core concepts of object oriented programming language. It is a mechanism where you can to derive a class from another class for a hierarchy of classes that share a set of attributes and methods.

// 4) What is Polymorphism?
// The word polymorphism is used in various contexts and describes situations in which something occurs in several different forms. In computer science, it describes the concept that objects of different types can be accessed through the same interface. Each type can provide its own,independent implementation of this interface. It is one of the core concepts of object oriented programming